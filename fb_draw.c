/*
 * fb_draw.c - Framebuffer drawing primitives and bitmap font
 */
#include "pinote.h"

static int current_orientation = 0;

// Optional background image, pre-transformed to the physical framebuffer layout.
// When set, clear_screen() memcpys this into backbuf instead of doing a solid fill.
static uint8_t *bg_buffer = NULL;

// Initialize framebuffer
int fb_init(Framebuffer *fb, const char *device) {
    fb->fd = open(device, O_RDWR);
    if (fb->fd == -1) return -1;

    if (ioctl(fb->fd, FBIOGET_VSCREENINFO, &fb->vinfo) == -1) {
        close(fb->fd);
        return -1;
    }

    if (ioctl(fb->fd, FBIOGET_FSCREENINFO, &fb->finfo) == -1) {
        close(fb->fd);
        return -1;
    }

    fb->screensize = fb->vinfo.yres * fb->finfo.line_length;
    fb->fbp = (uint8_t *)mmap(0, fb->screensize, PROT_READ | PROT_WRITE,
                              MAP_SHARED, fb->fd, 0);

    if (fb->fbp == MAP_FAILED) {
        close(fb->fd);
        return -1;
    }

    // Allocate back buffer for double-buffering
    fb->backbuf = (uint8_t *)malloc(fb->screensize);
    if (!fb->backbuf) {
        munmap(fb->fbp, fb->screensize);
        close(fb->fd);
        return -1;
    }

    // Set orientation from config (default: landscape)
    current_orientation = config.orientation > 0 ? config.orientation : ORIENTATION_LANDSCAPE;

    return 0;
}

// Get logical display dimensions based on orientation
void get_display_size(Framebuffer *fb, int *width, int *height) {
    if (current_orientation == ORIENTATION_PORTRAIT ||
        current_orientation == ORIENTATION_PORTRAIT_FLIP) {
        // Portrait: swap dimensions so layout logic works
        *width = fb->vinfo.yres;
        *height = fb->vinfo.xres;
    } else {
        *width = fb->vinfo.xres;
        *height = fb->vinfo.yres;
    }
}

// Transform coordinates based on orientation
static void transform_coords(Framebuffer *fb, int logical_x, int logical_y, int *physical_x, int *physical_y) {
    switch (current_orientation) {
        case ORIENTATION_LANDSCAPE:
            *physical_x = logical_x;
            *physical_y = logical_y;
            break;

        case ORIENTATION_PORTRAIT:
            *physical_x = fb->vinfo.xres - 1 - logical_y;
            *physical_y = logical_x;
            break;

        case ORIENTATION_LANDSCAPE_FLIP:
            *physical_x = fb->vinfo.xres - 1 - logical_x;
            *physical_y = fb->vinfo.yres - 1 - logical_y;
            break;

        case ORIENTATION_PORTRAIT_FLIP:
            *physical_x = logical_y;
            *physical_y = fb->vinfo.yres - 1 - logical_x;
            break;

        default:
            *physical_x = logical_x;
            *physical_y = logical_y;
            break;
    }
}

// Internal: write pixel to a target buffer (backbuf or fbp)
static void set_pixel_to(Framebuffer *fb, uint8_t *buf, int logical_x, int logical_y,
                         uint8_t r, uint8_t g, uint8_t b) {
    int x, y;
    transform_coords(fb, logical_x, logical_y, &x, &y);

    if (x < 0 || x >= (int)fb->vinfo.xres || y < 0 || y >= (int)fb->vinfo.yres)
        return;

    long location = y * fb->finfo.line_length + x * (fb->vinfo.bits_per_pixel / 8);

    if (fb->vinfo.bits_per_pixel == 32) {
        *(buf + location + 0) = b;
        *(buf + location + 1) = g;
        *(buf + location + 2) = r;
        *(buf + location + 3) = 0;
    } else if (fb->vinfo.bits_per_pixel == 24) {
        *(buf + location + 0) = b;
        *(buf + location + 1) = g;
        *(buf + location + 2) = r;
    } else if (fb->vinfo.bits_per_pixel == 16) {
        uint16_t rgb565 = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
        *((uint16_t *)(buf + location)) = rgb565;
    }
}

// Set pixel (writes to back buffer, with orientation support)
void set_pixel(Framebuffer *fb, int logical_x, int logical_y, uint8_t r, uint8_t g, uint8_t b) {
    set_pixel_to(fb, fb->backbuf, logical_x, logical_y, r, g, b);
}

// Fill rectangle (logical coordinates, orientation-aware)
void fill_rect(Framebuffer *fb, int x, int y, int w, int h, uint8_t r, uint8_t g, uint8_t b) {
    for (int row = y; row < y + h; row++) {
        for (int col = x; col < x + w; col++) {
            set_pixel(fb, col, row, r, g, b);
        }
    }
}

// Clear screen (back buffer) - fills first scanline, then memcpy to remaining rows
void clear_screen(Framebuffer *fb) {
    if (bg_buffer) {
        memcpy(fb->backbuf, bg_buffer, fb->screensize);
        return;
    }

    int bpp = fb->vinfo.bits_per_pixel / 8;
    int line_len = fb->finfo.line_length;
    uint8_t *first_row = fb->backbuf;

    // Fill first scanline with background color (R=20, G=20, B=25)
    for (int x = 0; x < (int)fb->vinfo.xres; x++) {
        if (bpp == 4) {
            first_row[x*4 + 0] = 25; first_row[x*4 + 1] = 20;
            first_row[x*4 + 2] = 20; first_row[x*4 + 3] = 0;
        } else if (bpp == 3) {
            first_row[x*3 + 0] = 25; first_row[x*3 + 1] = 20;
            first_row[x*3 + 2] = 20;
        } else if (bpp == 2) {
            uint16_t rgb565 = ((20 >> 3) << 11) | ((20 >> 2) << 5) | (25 >> 3);
            *((uint16_t *)(first_row + x*2)) = rgb565;
        }
    }

    // Copy first scanline to all remaining rows
    for (int y = 1; y < (int)fb->vinfo.yres; y++) {
        memcpy(fb->backbuf + y * line_len, first_row, line_len);
    }
}

// Load a 24-bit uncompressed BMP as the background image.
// The image is pre-transformed through the current orientation so clear_screen()
// can blit it via a single memcpy. Returns 0 on success, -1 on failure.
// Dimensions must match the logical display exactly (as reported by get_display_size).
int fb_load_background(Framebuffer *fb, const char *path) {
    if (!path || !path[0]) return -1;

    FILE *f = fopen(path, "rb");
    if (!f) {
        fprintf(stderr, "background: cannot open '%s'\n", path);
        return -1;
    }

    uint8_t fh[14], dh[40];
    if (fread(fh, 1, 14, f) != 14 || fh[0] != 'B' || fh[1] != 'M' ||
        fread(dh, 1, 40, f) != 40) {
        fprintf(stderr, "background: '%s' is not a valid BMP\n", path);
        fclose(f);
        return -1;
    }

    uint32_t data_off = fh[10] | (fh[11]<<8) | (fh[12]<<16) | (fh[13]<<24);
    int32_t bmp_w = (int32_t)(dh[4] | (dh[5]<<8) | (dh[6]<<16) | (dh[7]<<24));
    int32_t bmp_h = (int32_t)(dh[8] | (dh[9]<<8) | (dh[10]<<16) | (dh[11]<<24));
    uint16_t bpp_bmp = dh[14] | (dh[15]<<8);
    uint32_t compression = dh[16] | (dh[17]<<8) | (dh[18]<<16) | (dh[19]<<24);

    int top_down = 0;
    int h_abs = bmp_h;
    if (h_abs < 0) { top_down = 1; h_abs = -h_abs; }

    if (bpp_bmp != 24 || compression != 0) {
        fprintf(stderr, "background: only 24-bit uncompressed BMP is supported "
                        "(got %u bpp, compression=%u)\n", bpp_bmp, compression);
        fclose(f);
        return -1;
    }

    int disp_w, disp_h;
    get_display_size(fb, &disp_w, &disp_h);
    if (bmp_w != disp_w || h_abs != disp_h) {
        fprintf(stderr, "background: size mismatch — image is %dx%d, "
                        "display is %dx%d (logical). Skipping.\n",
                bmp_w, h_abs, disp_w, disp_h);
        fclose(f);
        return -1;
    }

    // Allocate the oriented buffer (matches backbuf layout). Calloc gives us
    // a sensible zero fill for any stride padding the framebuffer has.
    bg_buffer = (uint8_t *)calloc(1, fb->screensize);
    if (!bg_buffer) {
        fclose(f);
        return -1;
    }

    int row_padded = (bmp_w * 3 + 3) & ~3;
    uint8_t *row = (uint8_t *)malloc(row_padded);
    if (!row) {
        free(bg_buffer);
        bg_buffer = NULL;
        fclose(f);
        return -1;
    }

    if (fseek(f, (long)data_off, SEEK_SET) != 0) {
        free(row);
        free(bg_buffer);
        bg_buffer = NULL;
        fclose(f);
        return -1;
    }

    for (int ry = 0; ry < h_abs; ry++) {
        if (fread(row, 1, row_padded, f) != (size_t)row_padded) {
            fprintf(stderr, "background: short read at row %d\n", ry);
            free(row);
            free(bg_buffer);
            bg_buffer = NULL;
            fclose(f);
            return -1;
        }
        // BMP is bottom-up unless height is negative
        int logical_y = top_down ? ry : (h_abs - 1 - ry);
        for (int x = 0; x < bmp_w; x++) {
            uint8_t b = row[x*3 + 0];
            uint8_t g = row[x*3 + 1];
            uint8_t r = row[x*3 + 2];
            set_pixel_to(fb, bg_buffer, x, logical_y, r, g, b);
        }
    }

    free(row);
    fclose(f);
    fprintf(stderr, "background: loaded '%s' (%dx%d)\n", path, bmp_w, h_abs);
    return 0;
}

// Copy back buffer to framebuffer in one shot (no flicker)
void fb_flip(Framebuffer *fb) {
    memcpy(fb->fbp, fb->backbuf, fb->screensize);
}

// Set pixel directly on the front buffer (for sprite animation)
void set_pixel_front(Framebuffer *fb, int logical_x, int logical_y, uint8_t r, uint8_t g, uint8_t b) {
    set_pixel_to(fb, fb->fbp, logical_x, logical_y, r, g, b);
}

// Restore a single pixel from backbuf to front buffer
void fb_restore_pixel(Framebuffer *fb, int logical_x, int logical_y) {
    int x, y;
    transform_coords(fb, logical_x, logical_y, &x, &y);
    if (x < 0 || x >= (int)fb->vinfo.xres || y < 0 || y >= (int)fb->vinfo.yres)
        return;
    int bpp = fb->vinfo.bits_per_pixel / 8;
    long loc = y * fb->finfo.line_length + x * bpp;
    memcpy(fb->fbp + loc, fb->backbuf + loc, bpp);
}

// Restore a logical rect from backbuf to front buffer
void fb_restore_rect(Framebuffer *fb, int rx, int ry, int rw, int rh) {
    int bpp = fb->vinfo.bits_per_pixel / 8;
    for (int ly = ry; ly < ry + rh; ly++) {
        for (int lx = rx; lx < rx + rw; lx++) {
            int x, y;
            transform_coords(fb, lx, ly, &x, &y);
            if (x < 0 || x >= (int)fb->vinfo.xres || y < 0 || y >= (int)fb->vinfo.yres)
                continue;
            long loc = y * fb->finfo.line_length + x * bpp;
            memcpy(fb->fbp + loc, fb->backbuf + loc, bpp);
        }
    }
}

// Draw line (Bresenham with circular thickness)
void draw_line(Framebuffer *fb, int x0, int y0, int x1, int y1, uint8_t r, uint8_t g, uint8_t b, int thickness) {
    int dx = abs(x1 - x0);
    int dy = abs(y1 - y0);
    int sx = x0 < x1 ? 1 : -1;
    int sy = y0 < y1 ? 1 : -1;
    int err = dx - dy;
    int radius = thickness / 2;
    int r2 = radius * radius;

    while (1) {
        for (int ty = -radius; ty <= radius; ty++) {
            for (int tx = -radius; tx <= radius; tx++) {
                if (tx*tx + ty*ty <= r2) {
                    set_pixel(fb, x0 + tx, y0 + ty, r, g, b);
                }
            }
        }

        if (x0 == x1 && y0 == y1) break;

        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x0 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y0 += sy;
        }
    }
}

